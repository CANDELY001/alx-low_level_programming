/**
 * op_add - function returns sum
 * @a: parameter
 * @b: parameter
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function returns difference
 * @a: parameter
 * @b: parameter
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function returns product
 * @a: parameter
 * @b: parameter
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function returns division
 * @a: parameter
 * @b: parameter
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function returns remainder division
 * @a: parameter
 * @b: parameter
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
